public class Student {
    int ID;
    String name;
    String degree;
    int year;
    float GPA;
    public Student(int ID, String name,String degree,int year,float GPA){
        this.ID = ID;
        this.name = name;
        this.degree = degree;
        this.year = year;
        this.GPA = GPA;
    }
}
